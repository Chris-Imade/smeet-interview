export function getUpcomingYantra(): { date: string; time: string }[] {
    const now = new Date();
    const currentMinute = now.getMinutes();
    
    // Define start and end times
    const startTimeToday = new Date(now.getFullYear(), now.getMonth(), now.getDate(), 8, 0); // 8:00 AM today
    const endTimeToday = new Date(now.getFullYear(), now.getMonth(), now.getDate(), 23, 0); // 11:00 PM today
    const endTimeTomorrow = new Date(now.getFullYear(), now.getMonth(), now.getDate() + 1, 21, 15); // 09:15 PM tomorrow
    
    let upcomingYantra: { date: string; time: string }[] = [];
    let currentTime = new Date(now);

    // Adjust the current time to the next 15-minute interval
    currentTime.setMinutes(currentMinute + (15 - (currentMinute % 15)));
    currentTime.setSeconds(0);
    currentTime.setMilliseconds(0);

    // Generate times for today
    while (currentTime <= endTimeToday) {
        upcomingYantra.push({
            date: currentTime.toLocaleDateString(),
            time: currentTime.toLocaleTimeString([], { hour: '2-digit', minute: '2-digit', hour12: true }).toUpperCase()
        });
        currentTime.setMinutes(currentTime.getMinutes() + 15);
    }

    // Generate times for tomorrow
    currentTime = new Date(now.getFullYear(), now.getMonth(), now.getDate() + 1, 8, 0); // Start at 8:00 AM tomorrow
    while (currentTime <= endTimeTomorrow) {
        upcomingYantra.push({
            date: currentTime.toLocaleDateString(),
            time: currentTime.toLocaleTimeString([], { hour: '2-digit', minute: '2-digit', hour12: true }).toUpperCase()
        });
        currentTime.setMinutes(currentTime.getMinutes() + 15);
    }

    return upcomingYantra;
}

